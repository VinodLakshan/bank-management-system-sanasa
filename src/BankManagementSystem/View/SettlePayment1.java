    private void txtLoanIdActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtLoanIdActionPerformed
        String LID=txtLoanId.getText();
        try {
            LoanModel loan=LoanController.searchLoan(LID);
            if(loan != null){
                String AID = loan.getAID();
                AccountModel acount=AccountController.searchAccount(AID);
                String CID=acount.getCID();
                CustomerModel cst=CustomerController.searchCustomer(CID);
                String Name =cst.getfName()+" "+cst.getlName();
                String lnType=loan.getLoanType();
                LoanTypeModel lnModel=LoanTypeController.searchLoanType(lnType);
                String loanType=lnModel.getName();
                String loanAmount = Double.toString(loan.getAmount());
                String monthlyInstallment = Double.toString(loan.getPeriodical());
                String toBePaid = Double.toString(loan.getToBePaid());
                
                txtAccontId.setText(AID);
                txtName.setText(Name);
                txtLoanType.setText(loanType);
                txtLoanAmount.setText(loanAmount);
                txtMonthlyInstalment.setText(monthlyInstallment);
                txtAmountToBePaid.setText(toBePaid);
            }else{
                JOptionPane.showMessageDialog(this,"Loan ID is Incorrect..");
            }
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(SettlePayment1.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(SettlePayment1.class.getName()).log(Level.SEVERE, null, ex);
        }
        if(txtLoanAmount.getText().trim().length()>0){
            jButton2.setEnabled(true);
        }
    }//GEN-LAST:event_txtLoanIdActionPerformed

