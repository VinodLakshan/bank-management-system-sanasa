    private void txtSearchKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtSearchKeyReleased
        
        String which = txtSearch.getText();
        DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
        if (searchCombo.getSelectedItem().equals("Account ID")) {
            
            try {
                ArrayList<LoanModel> lnList = LoanController.searchLoanArray(which);
                dtm.setRowCount(0);
                if(lnList != null){
                    for (LoanModel ln : lnList) {
                        String lnType=ln.getLoanType();
                        LoanTypeModel lnT=LoanTypeController.searchLoanType(lnType);
                        AccountModel actm=AccountController.searchAccount(which);
                        CustomerModel cst=CustomerController.searchCustomer(actm.getCID());
                        Object[] row = {ln.getLID(),ln.getAID(),cst.getfName()+" "+cst.getlName(),lnT.getName(),ln.getAmount(),ln.getPeriodical(),ln.getToBePaid()};
                        dtm.addRow(row);
                    }
                }
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(ExistingCustomer.class.getName()).log(Level.SEVERE, null, ex);
            } catch (SQLException ex) {
                Logger.getLogger(ExistingCustomer.class.getName()).log(Level.SEVERE, null, ex);
            }
        }else{
           try {
                ArrayList<LoanModel> lnList = LoanController.searchLoanArrayCustomer(which);
                dtm.setRowCount(0);
                if(lnList != null){
                    for (LoanModel ln : lnList) {
                        String lnType=ln.getLoanType();
                        AccountModel actm=AccountController.searchAccount(ln.getAID());
                        CustomerModel cst=CustomerController.searchCustomer(actm.getCID());
                        LoanTypeModel lnT=LoanTypeController.searchLoanType(lnType);
                        String customerName=cst.getfName()+" "+cst.getlName();
                        Object[] row = {ln.getLID(),ln.getAID(),customerName,lnT.getName(),ln.getAmount(),ln.getPeriodical(),ln.getToBePaid()};
                        dtm.addRow(row);
                    }
                }
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(ExistingCustomer.class.getName()).log(Level.SEVERE, null, ex);
            } catch (SQLException ex) {
                Logger.getLogger(ExistingCustomer.class.getName()).log(Level.SEVERE, null, ex);
            }
                    
        }
    }//GEN-LAST:event_txtSearchKeyReleased

