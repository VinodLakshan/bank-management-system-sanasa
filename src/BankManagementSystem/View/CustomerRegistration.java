    private void NICtxtActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_NICtxtActionPerformed
        if(NICtxt.getText() != null){
            String NIC=NICtxt.getText();
            String syy=NIC.substring(0,2);
            String smm=NIC.substring(2,5);
            int yy=Integer.parseInt(19+syy);
            int mm=Integer.parseInt(smm);
            
            if(mm>=500){
		female.setSelected(true);
                mm-=500;
            }else{
                male.setSelected(true);
            }	
            
            if(yy % 4 != 0){
                mm=mm-1;
            }
            
            Calendar c1=Calendar.getInstance();
            c1.set(Calendar.YEAR, yy);
            c1.set(Calendar.DAY_OF_YEAR, mm);
            Date d1=c1.getTime();
            DOBtxt.setDate(d1);
            
            Date date=new Date();
            SimpleDateFormat curDate=new SimpleDateFormat("yyyy");
            String newDate=curDate.format(date);
            int year=Integer.parseInt(newDate);
            
            int age = year-yy;
            agetxt.setValue(age);
            addresstxt.requestFocus();
        }else{
            NICtxt.setText("None");
        }
    }//GEN-LAST:event_NICtxtActionPerformed

