    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String CID = CIDtxt.getText();
       
            String fName = fNametxt.getText();
               String lName = lNametxt.getText();
               SimpleDateFormat sdf = new SimpleDateFormat("YYYY-MM-dd");
               String DOB=sdf.format(DOBtxt.getDate());
               String age =Integer.toString((int) agetxt.getValue());
               String gender=null;
               if(male.isSelected()){
                   gender="male";
               }else{
                   gender="female";
               }
               String address = addresstxt.getText();
               String NIC = NICtxt.getText();
               String occ = occtxt.getText();
               String mbo = mbotxt.getText();
       
               CustomerModel cst=new CustomerModel(CID,fName,lName,DOB,age,gender,address,occ,NIC,mbo);
        
               try {
                   boolean res=CustomerController.addCustomer(cst);
                   if(res){
                       JOptionPane.showMessageDialog(this,"Customer Added Successfully..!!");
                   }else{
                JOptionPane.showMessageDialog(this,"Customer Added Failed..!!");
                   }
               } catch (ClassNotFoundException ex) {
                   Logger.getLogger(CustomerRegistration.class.getName()).log(Level.SEVERE, null, ex);
               } catch (SQLException ex) {
                   Logger.getLogger(CustomerRegistration.class.getName()).log(Level.SEVERE, null, ex);
               }        
               
        
               OpenAnAccount a1 = new OpenAnAccount();
               this.hide();
               //mainPanel.removeAll();
               mainPanel.add(a1);
               a1.setVisible(true);
               a1.txtId.setText(CID);
               a1.txtName.setText(fName+" "+lName);
               a1.txtDate.setText(datelbl.getText());
        
    }//GEN-LAST:event_jButton2ActionPerformed

