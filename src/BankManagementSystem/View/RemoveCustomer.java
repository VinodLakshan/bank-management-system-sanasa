    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        String CID=CIDtxt.getText();
        char[] pass={'a'};
        if(jPasswordField1.getPassword()==pass){
            jPanel4.setVisible(false);
            
               
            boolean res;
            try {
                res = CustomerController.deleteCustomer(CID);
                if(res){
                    JOptionPane.showMessageDialog(this,"Customer Deleted Successfully..!!");
                    Homepage h1 = new Homepage();
                    mainPanel.removeAll();
                    mainPanel.add(h1);
                    h1.setVisible(true);
            }else{
                JOptionPane.showMessageDialog(this,"Customer Delete Failed..!!");
            }        
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(RemoveCustomer.class.getName()).log(Level.SEVERE, null, ex);
            } catch (SQLException ex) {
                Logger.getLogger(RemoveCustomer.class.getName()).log(Level.SEVERE, null, ex);
            }
            
        }else{
            jPanel4.setVisible(false);
            JOptionPane.showMessageDialog(this,"Password Incorrect..");
            jPanel4.setVisible(true);
            jPasswordField1.requestFocus();
        }
        
    }//GEN-LAST:event_jButton5ActionPerformed

